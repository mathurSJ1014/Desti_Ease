def save_itinerary_to_file(city, itineraries):
    """
    Save the itinerary of the selected city to a text file with Day X format.
    """
    if city in itineraries:
        # Create a filename based on the city name
        filename = f"{city}_itinerary.txt"
        
        # Open the file in write mode and save the itinerary
        with open(filename, "w") as file:
            for day, activity in enumerate(itineraries[city], start=1):
                # Write each activity with "Day X: " prefix on a new line
                file.write(f"Day {day}: {activity}\n")
        
        print(f"The itinerary for {city} has been saved to '{filename}'.")
    else:
        print(f"City {city} not found.")

